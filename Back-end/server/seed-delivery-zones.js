async function seedDeliveryZones() {
  try {
    // Connect to MongoDB
    await mongoose.connect(process.env.MONGO_URI);
    console.log("✓ Connected to MongoDB");

    // Clear existing zones
    await DeliveryZone.deleteMany({});
    console.log("✓ Cleared existing delivery zones");

    // Insert new zones
    const insertedZones = await DeliveryZone.insertMany(deliveryZones);
    console.log(`✓ Inserted ${insertedZones.length} delivery zones`);

    // Display summary
    console.log("\n--- Delivery Zones Summary ---");
    for (const zone of insertedZones) {
      console.log(`\n${zone.name} (${zone.type}):`);
      console.log(`  - Delivery Time: ${zone.deliveryTime.minDays}-${zone.deliveryTime.maxDays} days`);
      console.log(`  - PIN Codes: ${zone.pinCodes.length}`);
      console.log(`  - Cities: ${zone.cities.length}`);
      console.log(`  - Shipping: ₹${zone.shippingCost.baseRate} + ₹${zone.shippingCost.perKgRate}/kg`);
    }
    console.log("\n✓ Seed completed successfully!");
    
    process.exit(0);
  } catch (error) {
    console.error("✗ Seed failed:", error);
    process.exit(1);
  }
}

// Run seeder
seedDeliveryZones();
