// One-time generator: extracts the authoritative hub + subcategory structure from
// import-autobacs-categories.js and emits config/canonicalTaxonomy.js.
// Re-run if the source structure ever changes. No DB access.
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const src = fs.readFileSync(path.join(__dirname, '../import-autobacs-categories.js'), 'utf8');

function extractArray(name) {
  const start = src.indexOf(`const ${name} = [`);
  if (start === -1) throw new Error(`array ${name} not found`);
  const open = src.indexOf('[', start);
  // find matching closing bracket
  let depth = 0, i = open;
  for (; i < src.length; i++) {
    if (src[i] === '[') depth++;
    else if (src[i] === ']') { depth--; if (depth === 0) break; }
  }
  const literal = src.slice(open, i + 1);
  // trusted repo file; literal is plain object array with // comments
  // eslint-disable-next-line no-eval
  return eval(`(${literal})`);
}

const hubs = extractArray('categoryStructure');       // {name, slug, description, order}
const leaves = extractArray('subCategoryStructure');  // {name, slug, parent, order}

const hubNameToSlug = new Map(hubs.map(h => [h.name, h.slug]));

// leaf slug -> hub slug
const leafSlugToHubSlug = {};
for (const leaf of leaves) {
  const hubSlug = hubNameToSlug.get(leaf.parent);
  if (!hubSlug) continue; // skip the stray 'parentCategory._id' artifact
  leafSlugToHubSlug[leaf.slug] = hubSlug;
}

const out = `// AUTO-GENERATED by scripts/gen-canonical-taxonomy.js — do not edit by hand.
// Canonical 2-level taxonomy (hub -> leaf) extracted from import-autobacs-categories.js.
// Source of truth for depth enforcement, re-parenting, and admin UI guidance.

export const HUBS = ${JSON.stringify(hubs.map(h => ({ name: h.name, slug: h.slug })), null, 2)};

export const HUB_SLUGS = new Set(HUBS.map(h => h.slug));

// Leaf category slug -> its canonical hub slug.
export const LEAF_SLUG_TO_HUB_SLUG = ${JSON.stringify(leafSlugToHubSlug, null, 2)};

export default { HUBS, HUB_SLUGS, LEAF_SLUG_TO_HUB_SLUG };
`;

const target = path.join(__dirname, '../config/canonicalTaxonomy.js');
fs.writeFileSync(target, out);
console.log(`Wrote ${target}`);
console.log(`Hubs: ${hubs.length}, leaf mappings: ${Object.keys(leafSlugToHubSlug).length}`);
